#include<iostream>

using namespace std;

int main() {

	int x = 10;
	int y = 20;

	cout << "x == y : " << (x == y) << endl;
	cout << "x != y : " << (x != y) << endl;
	cout << "x > y : "  << (x >  y) << endl;
	cout << "x >= y : " << (x >= y) << endl;
	cout << "x < y : "  << (x <  y) << endl;
	cout << "x <= y : " << (x <= y) << endl;  

	
	return 0;
}